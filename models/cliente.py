class Cliente():
    def __init__(self, email, ultimo_nome, primeiro_nome, senha, cidade,
                 bairro, rua, numero):
        self.id = 0
        self.email = email
        self.ultimo_nome = ultimo_nome
        self.primeiro_nome = primeiro_nome
        self.senha = senha
        self.cidade = cidade
        self.bairro = bairro
        self.rua = rua
        self.numero = numero

